class array_length {
  public static void main(String[] args) {
    int a1[]=new int[10];
    int a2[]={1,2,3,4,5,6,-10};
    int a3[]={4,3,2,1};
    System.out.println("Length of a1 : " + a1.length);
    System.out.println("Length of a2 : " + a2.length);
    System.out.println("Length of a3 : " + a3.length);
  }
}
